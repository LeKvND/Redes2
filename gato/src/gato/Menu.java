    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int Juego[] = new int[]{9,9,9,9,9,9,9,9,9};

        int numero = Integer.parseInt(jTextField2.getText());
        
        Tablero[] tableros = new Tablero[numero];
        Tablero_1[] tableros_1 = new Tablero_1[numero];                
        tache[] taches = new tache[numero];   
        circulo[] circulos = new circulo[numero]; 
        Gato[] gatos = new Gato[numero]; 
        
        for(int i=0;i<numero;i++){
            tableros[i] = new Tablero();
            tableros_1[i] = new Tablero_1();
            gatos[i] = new Gato(tableros[i],tableros_1[i]);
            gatos[i].start();
            taches[i] = new tache(tableros[i],tableros_1[i]);
            taches[i].start();
            circulos[i] = new circulo(tableros[i],tableros_1[i]);
            circulos[i].start();
            
            if(i<3){
                
                tableros[i].localizacion(0, i*295);
                tableros_1[i].localizacion(263,i*295);
                
            }else if(i<6){
                
                tableros[i].localizacion(526,(i-3)*295);
                tableros_1[i].localizacion(789,(i-3)*295);
                
            }else{
                
                tableros[i].localizacion(1050,0);
                tableros_1[i].localizacion(1050,0);
                
            }
            
            tableros[i].setVisible(true);
            tableros_1[i].setVisible(true);
        }
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

